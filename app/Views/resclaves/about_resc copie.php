<h3> À propos </h3><br>


<p class= "para">

<b>Présentation</b>
<br><br>
L'idée de ce site internet m'est venue au début de mes recherches pour mon doctorat. Je travaille sur les récits
d'esclaves publiés avant la guerre de Sécession et je me suis vite rendu compte que la liste des récits ne fait pas
consensus parmi les chercheurs et ceux qui en proposent une liste n'expliquant pas vraiment sur quels critères
sont inclus et les listes diffèrent entres elles. Le but de ce site est donc  de présenter une liste la plus 
exhaustive possible des récits qui sont cités par les différents chercheurs mais avec des commentaires sur ce qui fait qu'ils sont (ou pas) des récits d'esclaves.
Pour l'instant nous nous concentrons sur les récits écrits avant la guerre de Sécession mais il n'est pas exclu que le site évolue et qu'il englobe
également les récits écrits après.
